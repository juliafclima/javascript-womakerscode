// OPERADORES ARITMETICOS 

let num1 = 20;
let num2 = 10;

// ADIÇÃO +

let totalAdicao = num1 + num2;

console.log(`SOMA: ${num1} + ${num2} = ${totalAdicao}`);

// SUBTRAÇÃON -

let totalSubtracao = num1 - num2;

console.log(`SUBTRAÇÃO: ${num1} - ${num2} = ${totalSubtracao}`);

// MULTIPLICACAO *

let totalMultiplicacao = num1 * num2;

console.log(`MULTIPLICAÇÃO: ${num1} * ${num2} = ${totalMultiplicacao}`);

// DIVISÃO /

let totalDivisao = num1 / num2;

console.log(`DIVISÃO: ${num1} / ${num2} = ${totalDivisao}`);

// RESTO DA DIVISÃO % (MOD)

let totalResto = num1 % num2;
console.log(`RESTO: ${num1} / ${num2} = ${totalResto}`);

// INCREMENTO ++
console.log(`INCREMENTO: ${num1} -> ${++num1}`);

// DECREMENTO --
console.log(`DECREMENTO: ${num2} -> ${--num2}`);