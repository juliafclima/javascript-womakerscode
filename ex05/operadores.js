// OPERADORES ARITMETICOS 

let num1 = 10;
let num2 = 20;

// ADIÇÃO +

let totalAdicao = num1 + num2;

console.log(`${num1} + ${num2} = ${totalAdicao}`);

// SUBTRAÇÃON -

let totalSubtracao = num1 - num2;

console.log(`${num1} - ${num2} = ${totalSubtracao}`);

// MULTIPLICACAO *

let totalMultiplicacao = num1 * num2;

console.log(`${num1} * ${num2} = ${totalMultiplicacao}`);

// DIVISÃO /

let totalDivisao = num2 / num1;

console.log(`${num2} / ${num1} = ${totalDivisao}`);

// RESTO DA DIVISÃO % (MOD)

let restoDivisao = num1 % num2;
console.log(restoDivisao);